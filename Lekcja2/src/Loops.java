public class Loops
{
    public static void main(String[] args) {

        // Pierwszy rodzaj - For loops

        // Uzywamy w momencie kiedy wiemy ile bedzie powtorzen tej petli
        // Powtorzenia to inaczej iteracje

        // Skladnia
        // Wpisanie liczb od 1 do 10
        for(int i = 1; i < 11; i++)
        {
            if(i % 2 == 0)
            {
                System.out.println (i);
            }

        }

        // Trzeba wypisac liczby do 1 do 20 podzielne przez 7







    }
}
