public class Main
{
    public static void main(String[] args) {
        //sout
        System.out.println ("Hello world!!!");
        System.out.println ("Hello world");
        System.out.println ("Hello world");

        System.out.println ("_________________________");
        System.out.println ();

        // Typ zmiennej nazwa zmiennej = wartość która przypisujemy

        String hello = "Hello world";
        System.out.println (hello);


        // Liczby całkowitych - int

        int liczba = 10;
        System.out.println (liczba);

        //float

        float mojFloat = 10.11f;
        System.out.println (mojFloat);

        //char - character

        char mojChar = 'a';
        System.out.println (mojChar);

        //boolean - prawda/falsz

        boolean a = true;
        boolean b = false;
        System.out.println (a);
        System.out.println (b);



        String imie = "Piotrek";
        System.out.println ("Hello " + imie);

        int wiek = 22;
        System.out.println ("Wiek: " + wiek);


        int a1 = 10;
        System.out.println (a1);
        a1 = 20;
        System.out.println (a1);

        int x = 100;
        int y = 200;

        int tmp = x;
        x = y;
        y = tmp;

        System.out.println (x);
        System.out.println (y);

        // naszym zadaniem jest to żeby zamienic te wartosci czyli na koncu chcemy zeby x = 200 oraz y = 100;
        // nie mozna napisac ze x = 200 oraz y = 100



    }

}
